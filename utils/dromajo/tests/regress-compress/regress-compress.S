/*
 * Regress invalid compressed instruction support
 *
 * This test executes each of the 49,152 compressed instructions and
 * checks that exactly the right ones are trapped as invalid
 * instructions.
 */

        .file   "regress-compress.c"
        .option nopic
        .section .text.startup,"ax",@progbits
        .globl  _start
        .type   _start, @function


_start:
        li      a1,1<<13
        csrr    a0,mstatus
        or      a0,a0,a1
        csrw    mstatus,a0
	// Set up traphandlers
        la      t0, trap_vector
        csrw    mtvec, t0
	la 	t0, save_area		//saves address for use in first exception
	csrw	mscratch, t0
	// Set up instruction area
	la	t0, instr_area + 2048	//load middle of instr_area so jumps can go backwards
	li	t1, 0x10000
	sw	t1, 0(t0)		//set first C instruction 0x0, followed by C.nop (0x1) to help with alignment
	// Construct JAL instruction manually
	la	t1, run_tests
	addi	t0, t0, 4		//find address where JAL instruction will be stored
	sub 	t1, t1, t0		//find relative jump offset from instr_area to run_tests
	andi	t2, t2, 0		//clear t2 and t0 to construct split imm for JAL
	andi	t0, t0, 0
	li	a0, 1048576
	and 	t0, t1, a0		//get 21st bit
	sll	t0, t0, 11		//shift into position
	or	t2, t2, t0		//set instr bit 32
	andi	t0, t1, 2046		//get 11-2 bits
	sll	t0, t0, 20		//shift into position
	or	t2, t2, t0		//set instr bits 31-22
	li	a0, 2048
	and	t0, t1, a0		//get 12th bit
	sll	t0, t0, 9		//shift into position
	or	t2, t2, t0		//set instr bit 21
	li	a0, 1044480
	and	t0, t1, a0 		//get 20-13 bits
	or	t2, t2, t0		//set instr bits 20-13
	ori	t2, t2, 111		//set opcode
	la	t0, instr_area + 2048	//load middle of instr_area so jumps can go backwards
	sw	t2, 4(t0)		//stores J instr into instr_area
	j	create_table		//stores prev spike created list for comparison, then jumps to instr_area + 2048 (starts w instr 0x0)

        .align	4

trap_vector:
	// Check for illegal instruction
	csrr	t1, mcause
	li	t2, 2
	bne	t2, t1, skip    
	// Otherwise operate normally
	csrr	t2, mepc		//grab illegal instruction address
	lhu	t2, 0(t2)		//load illegal instrucion
	bnez	t2, not_zero		//recovers from hitting empty memory from j/jal, or first known illegal instruction
	la	t1, run_tests		//if instr is 0x0, jumps back to run_tests
	csrw	mepc, t1
	mret
not_zero:	
	// Compares trapped instr (not 0x0) to hopefully corresponding one in table
	csrrw	t0, mscratch, t0	//grab current step of save_area address
	lhu	t1, 0(t0)		//grab instruction from table
	bne	t1, t2, _failure	//if they mismatche, exit failure
	addi	t0, t0, 2		//advances to next 16 bit space in table
	csrrw	t0, mscratch, t0	//store new step of save-area address
skip:
	// Return to run_tests
        csrr 	t1, mepc
        addi	t1, t1, 2
        csrw 	mepc, t1
        mret


run_tests:
        la	t0, instr_area + 2048
	lhu	t0, 0(t0)		//pull current instruction
	addi	t0, t0, 1
	// Skip non-compressed instructions
	li	t1, 3
	andi	t2, t0, 3
	bne	t2, t1, _compressed
avoid_stuck:
	addi	t0, t0, 1
_compressed:
	// check for J pc +0 so we don't get stuck in place
	li	t1, 0xa001
	beq	t1, t0, avoid_stuck
	// check for J pc + 6 so we aren't getting half the J run_tests instruction
	li	t1, 0xa019
	beq	t1, t0, avoid_stuck
	li	t1, 0x10000			//final instruction value out of bounds, 17th bit set
	beq 	t1, t0, _success		//hasn't exited as failure through all instr, success
	la	t1, instr_area + 2048	//save new instruction into middle of instr_area
	sh	t0, 0(t1)
	// Safe memory/jumps to avoid overwriting instructions/seg faults
	andi	t1, t1, 0
	andi	t2, t0, 3		//pull C opcode
	beq	t2, t1, set_C_regs_safe	//check opcode 00
	addi	t1, t1, 1
	beq	t2, t1, branch_check	//check opcode 01
	addi	t1, t1, 1
	beq	t2, t1, j_s_check	//check opcode 10
deploy_instr:
	j	instr_area + 2048	//jump to middle of instr_area where instruction is stored
	

_success:	
	// All stored isntr parsed and all illegal instr match
	li	t0, 0x01	//store success 0x1 into tohost
	la	t1, tohost
	sh	t0, 0(t1)
	j	sit_and_spin	// see below

_failure:
	// Intrsuction trapped didn't match table, exit failure
	li	t0, 0x03
	la	t1, tohost
	sh	t0, 0(t1)
	j	sit_and_spin	// FOR WHATEVER REASON TOHOST DOESNT EXIT AUTOMATICALLY
	
sit_and_spin:
	j	sit_and_spin	//spike doesn't like j pc + 0 so this helps it quit


set_C_regs_safe:
	// Makes register hold safe memory values for C loads/stores
	la	x8, safe_memory + 4096
	mv	x9, x8
	mv	x10, x8
	mv	x11, x8
	mv	x12, x8
	mv	x13, x8
	mv	x14, x8
	mv	x15, x8
clear_sp:
	la	x2, safe_memory + 4096	//for stack pointer based load/stores
	j	deploy_instr

branch_check:
	// Determines whether instruction is BEQZ or BNEZ, or neither!
	li	t1, 0xc000		//check for BEQZ or not a branch, BEQZ and BNEZ are 110, 111 respectively
	and	t2, t1, t0
	bne	t1, t2, deploy_instr	//not a branch
	li	t1, 0x2000		//check difference bit between BEQZ and BNEZ
	and	t2, t1, t0
	bne	t1, t2, set_C_regs_safe	//if BEQZ: set all regs != 0
	// Otherwise BNEZ, set all C regs = 0
	andi	x8, x8, 0
	mv	x9, x8
	mv	x10, x8
	mv	x11, x8
	mv	x12, x8
	mv	x13, x8
	mv	x14, x8
	mv	x15, x8
	j	deploy_instr

j_s_check:
	// Check if stack relative load/store or reg jump
	li	t1, 0xe000		//JR has only MSB bit set, must check top 3 bits
	and	t2, t1, t0
	li	t1, 0x8000
	bne	t1, t2, clear_sp	//not JR
	// If branch is not taken, it is a JR instr, set regs to run_tests to avoid jumping somewhere random
	la	x1, run_tests
	mv	x2, x1
	mv	x3, x1
	mv	x4, x1
	mv	x5, x1
	mv	x6, x1
	mv	x7, x1
	mv	x8, x1
